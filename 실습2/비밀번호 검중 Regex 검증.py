import re

def validate_password(password):
    """
    사용자가 입력한 비밀번호가 보안 규칙에 맞는지 검증하는 함수
    반환값: 안전하면 True, 조건을 만족하지 못하면 False
    """
    # [정규 표현식 조건]
    # (?=.*[a-z]) : 소문자 최소 1개
    # (?=.*[A-Z]) : 대문자 최소 1개
    # (?=.*\d)     : 숫자 최소 1개
    # (?=.*[!@#$%^&*(),.?\":{}|<>]) : 기호 최소 1개
    pattern = r"^(?=.*[a-z])(?=.*[A-Z])(?=.*\d)(?=.*[!@#$%^&*(),.?\":{}|<>]).+$"
    
    # 패턴 매칭 결과를 불리언(True/False) 값으로 반환합니다.
    return bool(re.match(pattern, password))


# --- 프로그램 실행 메인 루프 ---
print("=========================================")
print("  비밀번호 보안 검증 프로그램을 시작합니다.")
print("  종료하려면 '!quit'을 입력하세요.")
print("=========================================")

while True:
    # 1. 사용자로부터 비밀번호 입력받기
    user_input = input("\n검증할 비밀번호를 입력하세요: ")
    
    # 2. 종료 명령어(!quit)를 입력했는지 먼저 확인
    if user_input == "!quit":
        print("프로그램을 종료합니다. 안전한 비밀번호를 사용하세요!")
        break
        
    # 3. 입력값 검증 함수 호출
    if validate_password(user_input):
        print("★ [성공] 안전한 비밀번호입니다! 규칙을 모두 만족합니다.")
    else:
        print("✗ [실패] 비밀번호 규칙에 맞지 않습니다.")
        print("  ※ 조건: 영문 소문자, 대문자, 숫자, 특수문자가 최소 1개씩 모두 포함되어야 합니다.")